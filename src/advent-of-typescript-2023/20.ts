type Letters = {
  A: ["█▀█ ", "█▀█ ", "▀ ▀ "];
  B: ["█▀▄ ", "█▀▄ ", "▀▀  "];
  C: ["█▀▀ ", "█ ░░", "▀▀▀ "];
  E: ["█▀▀ ", "█▀▀ ", "▀▀▀ "];
  H: ["█ █ ", "█▀█ ", "▀ ▀ "];
  I: ["█ ", "█ ", "▀ "];
  M: ["█▄░▄█ ", "█ ▀ █ ", "▀ ░░▀ "];
  N: ["█▄░█ ", "█ ▀█ ", "▀ ░▀ "];
  P: ["█▀█ ", "█▀▀ ", "▀ ░░"];
  R: ["█▀█ ", "██▀ ", "▀ ▀ "];
  S: ["█▀▀ ", "▀▀█ ", "▀▀▀ "];
  T: ["▀█▀ ", "░█ ░", "░▀ ░"];
  Y: ["█ █ ", "▀█▀ ", "░▀ ░"];
  W: ["█ ░░█ ", "█▄▀▄█ ", "▀ ░ ▀ "];
  " ": ["░", "░", "░"];
  ":": ["#", "░", "#"];
  "*": ["░", "#", "░"];
};

type Split<
  Str,
  Separator extends string = "\n",
  Arr extends string[] = [],
> = Str extends `${infer A}${Separator}${infer B}`
  ? Split<B, Separator, [...Arr, A]>
  : [...Arr, Str];
type test_Split = Split<"aaa\nbbb\nccc">; // =>

type Flatten<Arr extends any[], Out extends any[] = []> = Arr extends [
  infer U,
  ...infer Rest,
]
  ? U extends Array<any>
    ? Flatten<[...Flatten<U>, ...Rest], Out>
    : Flatten<Rest, [...Out, U]>
  : Out;
type test_Flatten = Flatten<[["a1", "a2", "a3"], ["b1", "b2", "b3"]]>; // =>

type ToLetters<
  Str extends string,
  Out extends [string, string, string] = ["", "", ""],
> = Str extends `${infer Char extends keyof Letters}${infer Rest}`
  ? ToLetters<
      Rest,
      [
        `${Out[0]}${Letters[Char][0]}`,
        `${Out[1]}${Letters[Char][1]}`,
        `${Out[2]}${Letters[Char][2]}`,
      ]
    >
  : Out;
type test_ToLetters = ToLetters<"CHAT">; // =>

// A variant of Split<> that uses ToLetters<>
type SplitAndToLetters<
  Str extends string,
  Separator extends string = "\n",
  Arr extends unknown[] = [],
> = Str extends `${infer A}${Separator}${infer B}`
  ? SplitAndToLetters<B, Separator, [...Arr, ToLetters<A>]>
  : [...Arr, ToLetters<Str>];

type ToAsciiArt<T extends string> = Flatten<SplitAndToLetters<Uppercase<T>>>;

